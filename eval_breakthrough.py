def fetch_gutenberg(gid, max_chars=5000):
    """Fetch and clean a Gutenberg text excerpt."""
    url = f"https://www.gutenberg.org/cache/epub/{gid}/pg{gid}.txt"
    try:
        req = urllib.request.Request(url, headers={"User-Agent": "waivelets-eval/0.1"})
        raw = urllib.request.urlopen(req, timeout=15).read().decode("utf-8-sig", errors="replace")
    except Exception:
        return None

    for m in ["*** START OF THE PROJECT GUTENBERG", "*** START OF THIS PROJECT GUTENBERG", "***START OF"]:
        idx = raw.find(m)
        if idx >= 0:
            raw = raw[raw.index("\n", idx) + 1:]
            break
    for m in ["*** END OF", "End of the Project Gutenberg", "End of Project Gutenberg"]:
        idx = raw.find(m)
        if idx >= 0:
            raw = raw[:idx]
            break

    # Skip headers/ToC — find first real paragraph
    lines = raw.split("\n")
    for i, line in enumerate(lines[20:], 20):
        if len(line.strip()) > 80 and not line.strip().isupper() and not line.strip().startswith("CHAPTER"):
            raw = "\n".join(lines[i:])
            break

    raw = raw[:max_chars].strip()
    # Trim to last sentence
    for end in [". ", "! ", "? "]:
        idx = raw.rfind(end)
        if idx > len(raw) // 2:
            raw = raw[:idx + 1]
            break
    return raw


def generate_ai_text_from_existing():
    """Load AI texts from existing eval files and the inline corpus."""
    texts = []

    # Load from v2 eval results
    v2_path = Path("eval_ai_human_v2_results.json")
    if v2_path.exists():
        v2 = json.load(v2_path.open())
        for r in v2.get("results", []):
            if r["source"] == "ai":
                # We need the original text, which we stored in the eval scripts
                texts.append({"id": r["id"], "genre": r["genre"], "source": "eval_v2"})

    # Load from v1 eval
    v1_path = Path("eval_ai_vs_human_results.json")
    if v1_path.exists():
        v1 = json.load(v1_path.open())
        for r in v1.get("results", []):
            if r["source"] == "ai":
                texts.append({"id": r["id"], "genre": r.get("genre", "unknown"), "source": "eval_v1"})

    return texts


def run():
    print("=" * 70)
    print("BREAKTHROUGH EVAL: AI vs Human at Scale")
    print("=" * 70)

    results = []
    checkpoint_path = RESULTS_DIR / "checkpoint.jsonl"

    # Load checkpoint
    done_ids = set()
    if checkpoint_path.exists():
        for line in checkpoint_path.open():
            try:
                r = json.loads(line)
                done_ids.add(r["id"])
                results.append(r)
            except Exception:
                pass
        print(f"Resumed from checkpoint: {len(results)} samples done")

    # ── Phase 1: Human texts from Gutenberg ──
    print(f"\n{'='*70}")
    print(f"PHASE 1: Human texts ({len(GUTENBERG_IDS)} Gutenberg IDs)")
    print(f"{'='*70}")

    # Deduplicate
    seen_gids = set()
    deduped_gids = []
    for entry in GUTENBERG_IDS:
        if entry[0] not in seen_gids:
            seen_gids.add(entry[0])
            deduped_gids.append(entry)

    for gid, title, author in deduped_gids:
        rid = f"human_g{gid}"
        if rid in done_ids:
            continue

        text = fetch_gutenberg(gid)
        if not text:
            print(f"  SKIP {title} — fetch failed")
            continue

        sentences = split_sentences(text)
        if len(sentences) < 10:
            print(f"  SKIP {title} — only {len(sentences)} sentences")
            continue
        if len(sentences) > 500:
            sentences = sentences[:500]

        try:
            fp = fingerprint("", sentences=sentences)
            mode, dist = classify(fp)
            r = {
                "id": rid, "source": "human", "title": title, "author": author,
                "mode": mode, "distance": round(dist, 3),
                "fp": fp._asdict(), "n_sentences": len(sentences),
            }
            results.append(r)
            done_ids.add(rid)
            with open(checkpoint_path, "a") as f:
                f.write(json.dumps(r) + "\n")
            print(f"  HUM {title:35s} → {mode:14s} d={dist:.2f} ent={fp.basin_entropy:.2f} ({len(sentences)} sent)")
        except Exception as e:
            print(f"  ERR {title}: {e}")
        gc.collect()

    # ── Phase 2: AI texts (from existing eval + inline samples) ──
    print(f"\n{'='*70}")
    print("PHASE 2: AI texts")
    print(f"{'='*70}")

    # Import the AI samples from eval_ai_human_v2
    try:
        from eval_ai_human_v2 import AI_SAMPLES as V2_SAMPLES
        ai_texts = [(s["id"], s["genre"], s["text"]) for s in V2_SAMPLES]
    except ImportError:
        ai_texts = []

    # Import from eval_ai_vs_human
    try:
        from eval_ai_vs_human import AI_SAMPLES as V1_SAMPLES
        for s in V1_SAMPLES:
            sid = s["id"]
            if not any(a[0] == sid for a in ai_texts):
                ai_texts.append((sid, s["genre"], s["text"]))
    except ImportError:
        pass

    print(f"  Loaded {len(ai_texts)} AI texts from eval scripts")

    for sid, genre, text in ai_texts:
        rid = f"ai_{sid}"
        if rid in done_ids:
            continue

        sentences = split_sentences(text)
        if len(sentences) < 5:
            continue
        if len(sentences) > 500:
            sentences = sentences[:500]

        try:
            fp = fingerprint("", sentences=sentences)
            mode, dist = classify(fp)
            r = {
                "id": rid, "source": "ai", "genre": genre,
                "mode": mode, "distance": round(dist, 3),
                "fp": fp._asdict(), "n_sentences": len(sentences),
            }
            results.append(r)
            done_ids.add(rid)
            with open(checkpoint_path, "a") as f:
                f.write(json.dumps(r) + "\n")
            print(f"  AI  {sid:35s} → {mode:14s} d={dist:.2f} ent={fp.basin_entropy:.2f} ({len(sentences)} sent)")
        except Exception as e:
            print(f"  ERR {sid}: {e}")
        gc.collect()

    # ── Phase 3: Analysis ──
    ai = [r for r in results if r["source"] == "ai"]
    hum = [r for r in results if r["source"] == "human"]

    print(f"\n{'='*70}")
    print(f"ANALYSIS: {len(ai)} AI, {len(hum)} human = {len(results)} total")
    print(f"{'='*70}")

    if len(ai) < 10 or len(hum) < 10:
        print("Not enough samples. Need at least 10 of each.")
        return

    fp_keys = list(ai[0]["fp"].keys())
    ai_arr = np.array([[r["fp"][k] for k in fp_keys] for r in ai])
    hum_arr = np.array([[r["fp"][k] for k in fp_keys] for r in hum])
    all_arr = np.vstack([ai_arr, hum_arr])
    labels = np.array([1]*len(ai_arr) + [0]*len(hum_arr))

    # Feature comparison
    print(f"\n{'Feature':20s} {'AI':>10s} {'Human':>10s} {'Cohen d':>10s}")
    print("─" * 52)
    for i, key in enumerate(fp_keys):
        am, asd = np.mean(ai_arr[:,i]), np.std(ai_arr[:,i])
        hm, hsd = np.mean(hum_arr[:,i]), np.std(hum_arr[:,i])
        pooled = np.sqrt((asd**2 + hsd**2)/2)
        d = abs(am-hm)/pooled if pooled > 0 else 0
        star = " ***" if d>1 else " **" if d>0.5 else ""
        print(f"{key:20s} {am:10.4f} {hm:10.4f} {d:10.2f}{star}")

    # ── 5-Fold Cross-Validation ──
    print(f"\n{'='*70}")
    print("5-FOLD CROSS-VALIDATION")
    print(f"{'='*70}")

    indices = np.arange(len(all_arr))
    np.random.seed(42)
    np.random.shuffle(indices)

    k = 5
    fold_size = len(indices) // k
    fold_accs = []
    fold_aucs = []
    all_test_scores = []
    all_test_labels = []

    for fold in range(k):
        test_idx = indices[fold * fold_size:(fold + 1) * fold_size]
        train_idx = np.concatenate([indices[:fold * fold_size], indices[(fold + 1) * fold_size:]])

        X_train, y_train = all_arr[train_idx], labels[train_idx]
        X_test, y_test = all_arr[test_idx], labels[test_idx]

        # Compute weights from training set only
        train_mean = np.mean(X_train, axis=0)
        train_std = np.std(X_train, axis=0)
        train_std[train_std < 1e-8] = 1

        # Feature weights from training set Cohen's d
        ai_train = X_train[y_train == 1]
        hum_train = X_train[y_train == 0]
        weights = np.zeros(len(fp_keys))
        for i in range(len(fp_keys)):
            am = np.mean(ai_train[:, i])
            hm = np.mean(hum_train[:, i])
            pooled = np.sqrt((np.std(ai_train[:, i])**2 + np.std(hum_train[:, i])**2) / 2)
            if pooled > 0:
                weights[i] = (am - hm) / pooled

        # Score test set
        z_test = (X_test - train_mean) / train_std
        scores = z_test @ weights

        # Find optimal threshold on training scores
        z_train = (X_train - train_mean) / train_std
        train_scores = z_train @ weights
        best_acc = 0
        best_thresh = 0
        for pct in range(5, 96):
            t = np.percentile(train_scores, pct)
            preds = (train_scores > t).astype(int)
            acc = np.mean(preds == y_train)
            if acc > best_acc:
                best_acc = acc
                best_thresh = t

        # Evaluate on test set
        test_preds = (scores > best_thresh).astype(int)
        test_acc = np.mean(test_preds == y_test)
        fold_accs.append(test_acc)

        # AUC (simple trapezoidal)
        sorted_idx = np.argsort(-scores)
        sorted_labels = y_test[sorted_idx]
        tpr_list = [0]
        fpr_list = [0]
        tp = fp = 0
        total_p = np.sum(y_test == 1)
        total_n = np.sum(y_test == 0)
        for sl in sorted_labels:
            if sl == 1:
                tp += 1
            else:
                fp += 1
            tpr_list.append(tp / max(total_p, 1))
            fpr_list.append(fp / max(total_n, 1))
        auc = float(np.sum(np.diff(fpr_list) * (np.array(tpr_list[:-1]) + np.array(tpr_list[1:])) / 2))
        fold_aucs.append(auc)

        all_test_scores.extend(scores.tolist())
        all_test_labels.extend(y_test.tolist())

        print(f"  Fold {fold+1}: acc={test_acc:.1%} AUC={auc:.3f} (train: {len(train_idx)}, test: {len(test_idx)})")

    mean_acc = np.mean(fold_accs)
    std_acc = np.std(fold_accs)
    mean_auc = np.mean(fold_aucs)
    std_auc = np.std(fold_aucs)

    print(f"\n  MEAN ACCURACY: {mean_acc:.1%} ± {std_acc:.1%}")
    print(f"  MEAN AUC:      {mean_auc:.3f} ± {std_auc:.3f}")

    # ── Baseline comparison ──
    print(f"\n{'='*70}")
    print("BASELINE COMPARISONS")
    print(f"{'='*70}")

    # Baseline 1: random
    random_acc = max(len(ai), len(hum)) / (len(ai) + len(hum))
    print(f"  Random baseline (majority class): {random_acc:.1%}")

    # Baseline 2: text length (n_sentences)
    sent_counts = np.array([r["n_sentences"] for r in results])
    best_sent_acc = 0
    for pct in range(5, 96):
        t = np.percentile(sent_counts, pct)
        for d in [1, -1]:
            preds = (sent_counts * d > t * d).astype(int)
            acc = np.mean(preds == labels)
            if acc > best_sent_acc:
                best_sent_acc = acc
    print(f"  Sentence count baseline: {best_sent_acc:.1%}")

    # Baseline 3: single best feature
    best_single = 0
    best_feat = ""
    for i, key in enumerate(fp_keys):
        for pct in range(5, 96):
            t = np.percentile(all_arr[:, i], pct)
            for d in [1, -1]:
                preds = (all_arr[:, i] * d > t * d).astype(int)
                acc = np.mean(preds == labels)
                if acc > best_single:
                    best_single = acc
                    best_feat = key
    print(f"  Best single feature ({best_feat}): {best_single:.1%}")
    print(f"  Waivelets composite (5-fold CV): {mean_acc:.1%} ± {std_acc:.1%}")
    print(f"  Improvement over majority: +{(mean_acc - random_acc)*100:.1f}pp")
    print(f"  Improvement over sentence count: +{(mean_acc - best_sent_acc)*100:.1f}pp")

    # ── Feature importance ──
    print(f"\n{'='*70}")
    print("FEATURE IMPORTANCE (by absolute weight)")
    print(f"{'='*70}")

    # Retrain on full dataset for feature weights
    full_mean = np.mean(all_arr, axis=0)
    full_std = np.std(all_arr, axis=0)
    full_std[full_std < 1e-8] = 1
    full_weights = np.zeros(len(fp_keys))
    for i in range(len(fp_keys)):
        am = np.mean(ai_arr[:, i])
        hm = np.mean(hum_arr[:, i])
        pooled = np.sqrt((np.std(ai_arr[:, i])**2 + np.std(hum_arr[:, i])**2) / 2)
        if pooled > 0:
            full_weights[i] = (am - hm) / pooled

    ranked = sorted(zip(fp_keys, full_weights), key=lambda x: abs(x[1]), reverse=True)
    for feat, w in ranked:
        direction = "AI higher" if w > 0 else "Human higher"
        bar = "█" * int(abs(w) / max(abs(full_weights)) * 30)
        print(f"  {feat:20s} {w:+7.3f} ({direction:12s}) {bar}")

    # ── Export final classifier ──
    # Find best threshold on full dataset
    z_all = (all_arr - full_mean) / full_std
    full_scores = z_all @ full_weights
    best_full_acc = 0
    best_full_thresh = 0
    for pct in range(5, 96):
        t = np.percentile(full_scores, pct)
        preds = (full_scores > t).astype(int)
        acc = np.mean(preds == labels)
        if acc > best_full_acc:
            best_full_acc = acc
            best_full_thresh = t

    classifier = {
        "version": "breakthrough_v1",
        "method": "weighted_zscore_cv",
        "features": fp_keys,
        "z_mean": full_mean.tolist(),
        "z_std": full_std.tolist(),
        "weights": full_weights.tolist(),
        "threshold": round(float(best_full_thresh), 4),
        "train_accuracy": round(float(best_full_acc), 4),
        "cv_accuracy_mean": round(float(mean_acc), 4),
        "cv_accuracy_std": round(float(std_acc), 4),
        "cv_auc_mean": round(float(mean_auc), 4),
        "cv_auc_std": round(float(std_auc), 4),
        "n_ai": len(ai),
        "n_human": len(hum),
        "n_total": len(results),
        "feature_importance": {feat: round(float(w), 4) for feat, w in ranked},
    }

    # Save
    output = {
        "timestamp": time.strftime("%Y-%m-%d %H:%M:%S"),
        "results": results,
        "classifier": classifier,
        "baselines": {
            "majority_class": round(float(random_acc), 4),
            "sentence_count": round(float(best_sent_acc), 4),
            "best_single_feature": round(float(best_single), 4),
            "best_single_feature_name": best_feat,
        },
    }

    with open(RESULTS_DIR / "full_results.json", "w") as f:
        json.dump(output, f, indent=2)

    with open("ai_detector_params.json", "w") as f:
        json.dump(classifier, f, indent=2)

    print(f"\n{'='*70}")
    print("FINAL SUMMARY")
    print(f"{'='*70}")
    print(f"  Samples: {len(ai)} AI + {len(hum)} human = {len(results)} total")
    print(f"  5-fold CV accuracy: {mean_acc:.1%} ± {std_acc:.1%}")
    print(f"  5-fold CV AUC:      {mean_auc:.3f} ± {std_auc:.3f}")
    print(f"  Full-set accuracy:  {best_full_acc:.1%}")
    print(f"  Top feature:        {ranked[0][0]} (d={abs(ranked[0][1]):.2f})")
    print(f"  vs majority class:  +{(mean_acc - random_acc)*100:.1f}pp")
    print(f"\n  Saved to {RESULTS_DIR}/full_results.json")
    print(f"  Updated ai_detector_params.json")


