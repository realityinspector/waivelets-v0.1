def fetch_gutenberg_excerpt(gid: int, max_chars: int = 3000) -> str | None:
    """Fetch first ~3000 chars of usable text from Project Gutenberg."""
    import urllib.request
    url = f"https://www.gutenberg.org/cache/epub/{gid}/pg{gid}.txt"
    try:
        with urllib.request.urlopen(url, timeout=15) as resp:
            raw = resp.read().decode("utf-8", errors="replace")
    except Exception as e:
        print(f"  [WARN] Failed to fetch {gid}: {e}")
        return None

    # Strip Gutenberg header/footer
    start_markers = ["*** START OF", "***START OF"]
    end_markers = ["*** END OF", "***END OF"]
    text = raw
    for m in start_markers:
        idx = text.find(m)
        if idx >= 0:
            text = text[text.index("\n", idx) + 1:]
            break
    for m in end_markers:
        idx = text.find(m)
        if idx >= 0:
            text = text[:idx]
            break

    # Skip to first real paragraph (skip table of contents etc.)
    lines = text.split("\n")
    start = 0
    for i, line in enumerate(lines):
        stripped = line.strip()
        if len(stripped) > 80 and not stripped.startswith("CHAPTER") and not stripped.isupper():
            start = i
            break

    text = "\n".join(lines[start:])
    text = text[:max_chars]
    # Trim to last sentence
    for end in [". ", "! ", "? "]:
        idx = text.rfind(end)
        if idx > len(text) // 2:
            text = text[:idx + 1]
            break

    return text.strip()


