    ai_results = [r for r in results if r["source"] == "ai"]
    human_results = [r for r in results if r["source"] == "human"]

    print(f"\nSample sizes: AI={len(ai_results)}, Human={len(human_results)}")

    # Extract fingerprint arrays
    fp_keys = ["smoothness_mean", "smoothness_std", "exposition", "interiority",
                "formal_structure", "scene_narration", "basin_entropy"]

    ai_fps = np.array([[r["fp"][k] for k in fp_keys] for r in ai_results])
    human_fps = np.array([[r["fp"][k] for k in fp_keys] for r in human_results])

    print(f"\n{'Feature':20s} {'AI mean':>10s} {'AI std':>10s} {'Human mean':>12s} {'Human std':>10s} {'Δ/σ':>8s}")
    print("─" * 72)

    for i, key in enumerate(fp_keys):
        ai_mean = np.mean(ai_fps[:, i])
        ai_std = np.std(ai_fps[:, i])
        hum_mean = np.mean(human_fps[:, i])
        hum_std = np.std(human_fps[:, i])
        pooled_std = np.sqrt((ai_std**2 + hum_std**2) / 2)
        effect = abs(ai_mean - hum_mean) / pooled_std if pooled_std > 0 else 0
        marker = " ***" if effect > 1.0 else " **" if effect > 0.5 else " *" if effect > 0.3 else ""
        print(f"{key:20s} {ai_mean:10.4f} {ai_std:10.4f} {hum_mean:12.4f} {hum_std:10.4f} {effect:8.2f}{marker}")

    # Mode distributions
    from collections import Counter
    ai_modes = Counter(r["mode"] for r in ai_results)
    hum_modes = Counter(r["mode"] for r in human_results)

    print(f"\nMode distributions:")
    print(f"  {'Mode':16s} {'AI':>6s} {'AI%':>6s} {'Human':>8s} {'Human%':>8s}")
    print(f"  {'─'*46}")
    for mode in ["convergent", "contemplative", "discursive", "dialectical"]:
        ai_n = ai_modes.get(mode, 0)
        hum_n = hum_modes.get(mode, 0)
        ai_pct = 100 * ai_n / len(ai_results) if ai_results else 0
        hum_pct = 100 * hum_n / len(human_results) if human_results else 0
        print(f"  {mode:16s} {ai_n:6d} {ai_pct:5.1f}% {hum_n:8d} {hum_pct:7.1f}%")

    # Centroid distance between AI and Human clusters
    ai_centroid = np.mean(ai_fps, axis=0)
    hum_centroid = np.mean(human_fps, axis=0)
    centroid_dist = np.linalg.norm(ai_centroid - hum_centroid)

    # Average intra-class distance
    ai_intra = np.mean([np.linalg.norm(fp - ai_centroid) for fp in ai_fps])
    hum_intra = np.mean([np.linalg.norm(fp - hum_centroid) for fp in human_fps])

    print(f"\nCluster analysis:")
    print(f"  AI centroid:    [{', '.join(f'{v:.4f}' for v in ai_centroid)}]")
    print(f"  Human centroid: [{', '.join(f'{v:.4f}' for v in hum_centroid)}]")
    print(f"  Inter-centroid distance: {centroid_dist:.4f}")
    print(f"  AI avg intra-class dist: {ai_intra:.4f}")
    print(f"  Human avg intra-class dist: {hum_intra:.4f}")

    separation_ratio = centroid_dist / ((ai_intra + hum_intra) / 2)
    print(f"  Separation ratio (inter/intra): {separation_ratio:.3f}")

    if separation_ratio > 1.5:
        verdict = "STRONG separation — fingerprints differ systematically"
    elif separation_ratio > 0.8:
        verdict = "MODERATE separation — some signal, noisy"
    elif separation_ratio > 0.3:
        verdict = "WEAK separation — distributions overlap heavily"
    else:
        verdict = "NO separation — fingerprints are indistinguishable"

    print(f"\n  Verdict: {verdict}")

    # Simple binary classifier: try a threshold on each feature
    print(f"\n{'=' * 60}")
    print("PHASE 4: Can we build a classifier?")
    print("=" * 60)

    all_fps = np.vstack([ai_fps, human_fps])
    labels = np.array([1] * len(ai_fps) + [0] * len(human_fps))  # 1=AI, 0=human

    best_acc = 0
    best_feature = ""
    best_threshold = 0

    for i, key in enumerate(fp_keys):
        vals = all_fps[:, i]
        # Try 100 thresholds
        for pct in range(5, 96):
            thresh = np.percentile(vals, pct)
            # Try both directions
            for direction in [1, -1]:
                preds = (vals * direction > thresh * direction).astype(int)
                acc = np.mean(preds == labels)
                if acc > best_acc:
                    best_acc = acc
                    best_feature = key
                    best_threshold = thresh
                    best_dir = ">" if direction == 1 else "<"

    print(f"  Best single-feature classifier:")
    print(f"    Feature: {best_feature} {best_dir} {best_threshold:.4f}")
    print(f"    Accuracy: {best_acc:.1%} ({int(best_acc * len(labels))}/{len(labels)})")

    # L2 distance from AI centroid vs Human centroid
    ai_from_ai = np.array([np.linalg.norm(fp - ai_centroid) for fp in ai_fps])
    ai_from_hum = np.array([np.linalg.norm(fp - hum_centroid) for fp in ai_fps])
    hum_from_ai = np.array([np.linalg.norm(fp - ai_centroid) for fp in human_fps])
    hum_from_hum = np.array([np.linalg.norm(fp - hum_centroid) for fp in human_fps])

    # Classify by which centroid is closer
    ai_correct = np.sum(ai_from_ai < ai_from_hum)
    hum_correct = np.sum(hum_from_hum < hum_from_ai)
    centroid_acc = (ai_correct + hum_correct) / (len(ai_fps) + len(human_fps))

    print(f"\n  Centroid-distance classifier:")
    print(f"    AI correctly assigned to AI cluster:    {ai_correct}/{len(ai_fps)} ({100*ai_correct/len(ai_fps):.0f}%)")
    print(f"    Human correctly assigned to Human cluster: {hum_correct}/{len(human_fps)} ({100*hum_correct/len(human_fps):.0f}%)")
    print(f"    Overall accuracy: {centroid_acc:.1%}")

    # Save results
    output = {
        "timestamp": time.strftime("%Y-%m-%d %H:%M:%S"),
        "n_ai": len(ai_results),
        "n_human": len(human_results),
        "results": results,
        "analysis": {
            "centroid_distance": round(centroid_dist, 4),
            "ai_intra_dist": round(ai_intra, 4),
            "human_intra_dist": round(hum_intra, 4),
            "separation_ratio": round(separation_ratio, 3),
            "verdict": verdict,
            "best_single_feature_acc": round(best_acc, 3),
            "best_feature": best_feature,
            "centroid_classifier_acc": round(centroid_acc, 3),
        }
    }

    with open("eval_ai_vs_human_results.json", "w") as f:
        json.dump(output, f, indent=2)
    print(f"\nResults saved to eval_ai_vs_human_results.json")

    return output


