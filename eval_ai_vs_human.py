    results = []

    # ── Phase 1: Fingerprint AI texts ──
    print("=" * 60)
    print("PHASE 1: Fingerprinting AI-generated texts")
    print("=" * 60)

    for sample in AI_SAMPLES:
        t0 = time.time()
        try:
            fp = fingerprint(sample["text"])
            mode, dist = classify(fp)
            elapsed = (time.time() - t0) * 1000
            results.append({
                "source": "ai",
                "id": sample["id"],
                "genre": sample["genre"],
                "topic": sample["topic"],
                "mode": mode,
                "distance": round(dist, 3),
                "fp": fp._asdict(),
                "time_ms": round(elapsed, 1),
            })
            print(f"  AI  {sample['id']:30s} → {mode:14s} d={dist:.2f}  ({elapsed:.0f}ms)")
        except Exception as e:
            print(f"  AI  {sample['id']:30s} → ERROR: {e}")

    # ── Phase 2: Fingerprint human texts from corpus ──
    print(f"\n{'=' * 60}")
    print("PHASE 2: Fingerprinting human texts (Gutenberg)")
    print("=" * 60)

